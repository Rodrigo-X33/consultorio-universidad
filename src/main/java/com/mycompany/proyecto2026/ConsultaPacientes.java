    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String nombre = jTextField1.getText().trim();
        String edadTxt = jTextField2.getText().trim();
        String pesoTxt = jTextField3.getText().trim();
        String estaturaTxt = jTextField4.getText().trim();
        String telefonos = jTextField5.getText().trim();

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre del paciente.");
            return;
        }

        int edad;
        double peso;
        double estatura;
        try {
            edad = Integer.parseInt(edadTxt);
            if (edad < 0) {
                JOptionPane.showMessageDialog(this, "La edad no puede ser negativa.");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Edad inválida.");
            return;
        }
        try {
            peso = Double.parseDouble(pesoTxt);
            if (peso <= 0) {
                JOptionPane.showMessageDialog(this, "El peso debe ser mayor a 0.");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Peso inválido.");
            return;
        }
        try {
            estatura = Double.parseDouble(estaturaTxt);
            if (estatura <= 0) {
                JOptionPane.showMessageDialog(this, "La estatura debe ser mayor a 0.");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Estatura inválida.");
            return;
        }

        if (telefonos.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese al menos un teléfono.");
            return;
        }

        if (modificarPaciente != null) {
            modificarPaciente.nombre = nombre;
            modificarPaciente.edad = edad;
            modificarPaciente.peso = peso;
            modificarPaciente.estatura = estatura;
            modificarPaciente.telefonos = telefonos;
            JOptionPane.showMessageDialog(this, "Paciente modificado.");
            llenarTabla();
            modificarPaciente = null;
        } else {
            Paciente nuevo = new Paciente();
            nuevo.codigo = Proyecto2026.contadorPacientes++;
            nuevo.nombre = nombre;
            nuevo.edad = edad;
            nuevo.peso = peso;
            nuevo.estatura = estatura;
            nuevo.telefonos = telefonos;
            Proyecto2026.pacientes.add(nuevo);
            JOptionPane.showMessageDialog(this, "Paciente creado.");
            llenarTabla();
        }
        limpiarCampos();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (Proyecto2026.pacientes.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay pacientes para exportar.");
            return;
        }
        File downloads = obtenerCarpetaDownloads();
        File archivo = new File(downloads, "pacientes.json");
        try (FileWriter fw = new FileWriter(archivo)) {
            fw.write(construirJson());
            JOptionPane.showMessageDialog(this, "Pacientes exportados a:\n" + archivo.getAbsolutePath());
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al exportar JSON: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed


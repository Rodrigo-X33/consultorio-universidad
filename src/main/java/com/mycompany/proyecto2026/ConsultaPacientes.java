    private File obtenerCarpetaDownloads() {
        File downloads = new File(System.getProperty("user.home"), "Downloads");
        if (!downloads.exists()) {
            downloads.mkdirs();
        }
        return downloads;
    }

    private String construirJson() {
        StringBuilder sb = new StringBuilder();
        sb.append("{\n");
        sb.append("  \"pacientes\": [\n");
        for (int i = 0; i < Proyecto2026.pacientes.size(); i++) {
            Paciente p = Proyecto2026.pacientes.get(i);
            sb.append("    {\n");
            sb.append("      \"codigo\": ").append(p.codigo).append(",\n");
            sb.append("      \"nombre\": \"").append(escaparJson(p.nombre)).append("\",\n");
            sb.append("      \"edad\": ").append(p.edad).append(",\n");
            sb.append("      \"peso\": ").append(p.peso).append(",\n");
            sb.append("      \"estatura\": ").append(p.estatura).append(",\n");
            sb.append("      \"telefonos\": \"").append(escaparJson(p.telefonos)).append("\"\n");
            sb.append("    }");
            if (i < Proyecto2026.pacientes.size() - 1) sb.append(",");
            sb.append("\n");
        }
        sb.append("  ]\n");
        sb.append("}\n");
        return sb.toString();
    }

    private String escaparJson(String s) {
        if (s == null) return "";
        return s.replace("\\", "\\\\").replace("\"", "\\\"")
                .replace("\n", "\\n").replace("\r", "\\r").replace("\t", "\\t");
    }

    private void escribirXml(File archivo) throws ParserConfigurationException, TransformerException {
        DocumentBuilderFactory dbf = DocumentBuilderFactory.newInstance();
        DocumentBuilder db = dbf.newDocumentBuilder();
        Document doc = db.newDocument();

        Element raiz = doc.createElement("pacientes");
        doc.appendChild(raiz);

        for (Paciente p : Proyecto2026.pacientes) {
            Element pe = doc.createElement("paciente");
            pe.appendChild(crearElemento(doc, "codigo", String.valueOf(p.codigo)));
            pe.appendChild(crearElemento(doc, "nombre", p.nombre));
            pe.appendChild(crearElemento(doc, "edad", String.valueOf(p.edad)));
            pe.appendChild(crearElemento(doc, "peso", String.valueOf(p.peso)));
            pe.appendChild(crearElemento(doc, "estatura", String.valueOf(p.estatura)));
            pe.appendChild(crearElemento(doc, "telefonos", p.telefonos));
            raiz.appendChild(pe);
        }

        TransformerFactory tf = TransformerFactory.newInstance();
        Transformer t = tf.newTransformer();
        t.setOutputProperty(OutputKeys.INDENT, "yes");
        t.setOutputProperty(OutputKeys.ENCODING, "UTF-8");
        t.setOutputProperty("{http://xml.apache.org/xslt}indent-amount", "2");
        t.transform(new DOMSource(doc), new StreamResult(archivo));
    }

    private Element crearElemento(Document doc, String nombre, String valor) {
        Element e = doc.createElement(nombre);
        e.setTextContent(valor == null ? "" : valor);
        return e;
    }

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Seleccione el archivo XML de pacientes");
        chooser.setFileFilter(new FileNameExtensionFilter("Archivos XML (*.xml)", "xml"));
        File defaultFile = new File(System.getProperty("user.home"), "Documents/pacientes.xml");
        if (defaultFile.exists()) {
            chooser.setSelectedFile(defaultFile);
        } else {
            chooser.setCurrentDirectory(new File(System.getProperty("user.home"), "Documents"));
        }
        if (chooser.showOpenDialog(this) != JFileChooser.APPROVE_OPTION) {
            return;
        }

        File archivo = chooser.getSelectedFile();
        int importados = 0;
        int omitidos = 0;
        StringBuilder errores = new StringBuilder();

        try {
            DocumentBuilderFactory dbf = DocumentBuilderFactory.newInstance();
            DocumentBuilder db = dbf.newDocumentBuilder();
            Document doc = db.parse(archivo);
            doc.getDocumentElement().normalize();

            NodeList nodos = doc.getElementsByTagName("paciente");
            for (int i = 0; i < nodos.getLength(); i++) {
                Element el = (Element) nodos.item(i);
                int numPaciente = i + 1;

                String nombre = obtenerTexto(el, "nombre");
                String edadTxt = obtenerTexto(el, "edad");
                String pesoTxt = obtenerTexto(el, "peso");
                String estaturaTxt = obtenerTexto(el, "estatura");
                String telefonos = obtenerTexto(el, "telefonos");

                if (nombre.isEmpty()) {
                    omitidos++;
                    errores.append("Paciente ").append(numPaciente).append(": nombre vacío\n");
                    continue;
                }
                int edad;
                double peso, estatura;
                try {
                    edad = Integer.parseInt(edadTxt);
                    peso = Double.parseDouble(pesoTxt);
                    estatura = Double.parseDouble(estaturaTxt);
                } catch (NumberFormatException e) {
                    omitidos++;
                    errores.append("Paciente ").append(numPaciente).append(": valores numéricos inválidos\n");
                    continue;
                }
                if (edad < 0 || peso <= 0 || estatura <= 0) {
                    omitidos++;
                    errores.append("Paciente ").append(numPaciente).append(": valores fuera de rango\n");
                    continue;
                }

                Paciente nuevo = new Paciente();
                nuevo.codigo = Proyecto2026.contadorPacientes++;
                nuevo.nombre = nombre;
                nuevo.edad = edad;
                nuevo.peso = peso;
                nuevo.estatura = estatura;
                nuevo.telefonos = telefonos;
                Proyecto2026.pacientes.add(nuevo);
                importados++;
            }
        } catch (ParserConfigurationException | SAXException | IOException ex) {
            JOptionPane.showMessageDialog(this, "Error al leer el archivo XML: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        llenarTabla();
        String resumen = "Pacientes importados: " + importados + "\nOmitidos: " + omitidos;
        if (omitidos > 0) {
            resumen += "\n\nDetalle:\n" + errores.toString();
        }
        JOptionPane.showMessageDialog(this, resumen, "Carga XML", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton7ActionPerformed

    private String obtenerTexto(Element padre, String etiqueta) {
        NodeList lista = padre.getElementsByTagName(etiqueta);
        if (lista.getLength() == 0 || lista.item(0).getTextContent() == null) {
            return "";
        }
        return lista.item(0).getTextContent().trim();
    }


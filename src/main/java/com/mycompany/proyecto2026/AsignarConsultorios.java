    public void llenarCombos() {
        usuariosDisponibles = new ArrayList<>();
        DefaultComboBoxModel<String> modeloUsuarios = new DefaultComboBoxModel<>();
        for (Usuario u : Proyecto2026.usuarios) {
            if (!"Administrador".equals(u.rol)) {
                usuariosDisponibles.add(u);
                modeloUsuarios.addElement(u.nombre + " (" + u.rol + ")");
            }
        }
        jComboBox1.setModel(modeloUsuarios);
        jComboBox1.setSelectedIndex(-1);

        consultoriosDisponibles = new ArrayList<>();
        DefaultComboBoxModel<String> modeloConsultorios = new DefaultComboBoxModel<>();
        for (Consultorio c : Proyecto2026.consultorios) {
            consultoriosDisponibles.add(c);
            modeloConsultorios.addElement(String.format("%03d", c.codigo) + " - " + c.nombre);
        }
        jComboBox2.setModel(modeloConsultorios);
        jComboBox2.setSelectedIndex(-1);
    }


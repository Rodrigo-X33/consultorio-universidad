    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int idxUsuario = jComboBox1.getSelectedIndex();
        int idxConsultorio = jComboBox2.getSelectedIndex();

        if (idxUsuario < 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un usuario.");
            return;
        }
        if (idxConsultorio < 0) {
            JOptionPane.showMessageDialog(this, "Seleccione un consultorio.");
            return;
        }

        Usuario usuario = usuariosDisponibles.get(idxUsuario);
        Consultorio consultorio = consultoriosDisponibles.get(idxConsultorio);
        usuario.consultorio = consultorio;

        JOptionPane.showMessageDialog(this, "Usuario " + usuario.nombre + " asignado al consultorio " + String.format("%03d", consultorio.codigo) + " - " + consultorio.nombre + ".");
    }//GEN-LAST:event_jButton1ActionPerformed


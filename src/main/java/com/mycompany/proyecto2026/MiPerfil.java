    private String validarPassword(String password) {
        if (password.length() < 6) {
            return "La contrase\u00f1a debe tener al menos 6 caracteres.";
        }
        if (!password.matches(".*[0-9].*")) {
            return "La contrase\u00f1a debe contener al menos un n\u00famero.";
        }
        if (!password.matches(".*[A-Z].*")) {
            return "La contrase\u00f1a debe contener al menos una letra may\u00fascula.";
        }
        if (!password.matches(".*[a-z].*")) {
            return "La contrase\u00f1a debe contener al menos una letra min\u00fascula.";
        }
        return null;
    }


        String errorPassword = validarPassword(password);
        if(errorPassword != null){
            jLabel8.setText("<html>" + errorPassword + "</html>");
            jLabel8.setVisible(true);
            return;
        }

        jLabel8.setVisible(false);
        Proyecto2026.usuarioLogin.password = password;
        JOptionPane.showMessageDialog(this, "Contrase\u00f1a cambiada exitosamente.");
        jTextField1.setText("");

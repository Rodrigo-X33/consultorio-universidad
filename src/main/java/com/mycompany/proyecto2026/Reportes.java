    private void generarCitasAtendidas() {
        String[] columnas = {"Paciente", "Doctor", "Fecha y hora", "Motivo", "Estado"};
        DefaultTableModel modelo = new DefaultTableModel(columnas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        for (Cita c : Proyecto2026.citas) {
            if ("Atendida".equals(c.estado)) {
                Object[] fila = {
                    c.paciente.nombre,
                    c.doctor.nombre,
                    SDF.format(c.fechaHora),
                    c.motivo,
                    c.estado
                };
                modelo.addRow(fila);
            }
        }
        jTable1.setModel(modelo);
    }

    private void generarPacientesPorDoctor() {
        Usuario doctor = (Usuario) jComboBox2.getSelectedItem();
        if (doctor == null) {
            JOptionPane.showMessageDialog(this, "Seleccione un doctor.");
            return;
        }
        String[] columnas = {"Paciente", "Fecha de atención", "Consultorio", "Motivo de consulta"};
        DefaultTableModel modelo = new DefaultTableModel(columnas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        for (Cita c : Proyecto2026.citas) {
            if ("Atendida".equals(c.estado) && c.doctor == doctor) {
                Object[] fila = {
                    c.paciente.nombre,
                    SDF.format(c.fechaHora),
                    c.consultorio.nombre,
                    c.motivo
                };
                modelo.addRow(fila);
            }
        }
        jTable1.setModel(modelo);
    }


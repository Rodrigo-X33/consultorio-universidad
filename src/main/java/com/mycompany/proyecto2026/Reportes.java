    // Rodea con comillas si el valor contiene coma, comillas o salto de línea.
    private String escaparCsv(String valor) {
        if (valor.contains(",") || valor.contains("\"") || valor.contains("\n")) {
            return "\"" + valor.replace("\"", "\"\"") + "\"";
        }
        return valor;
    }


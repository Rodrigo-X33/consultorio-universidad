    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        if (modelo.getColumnCount() == 0) {
            JOptionPane.showMessageDialog(this, "Genere primero un reporte.");
            return;
        }
        if (modelo.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "El reporte no tiene filas para exportar.");
            return;
        }

        File downloads = new File(System.getProperty("user.home"), "Downloads");
        if (!downloads.exists()) {
            downloads.mkdirs();
        }
        String nombreArchivo = jComboBox1.getSelectedIndex() == 0
                ? "reporte_citas_atendidas.csv"
                : "reporte_pacientes_por_doctor.csv";
        File archivo = new File(downloads, nombreArchivo);

        try (FileWriter fw = new FileWriter(archivo)) {
            int columnas = modelo.getColumnCount();
            for (int c = 0; c < columnas; c++) {
                fw.write(escaparCsv(modelo.getColumnName(c)));
                if (c < columnas - 1) {
                    fw.write(",");
                }
            }
            fw.write("\n");

            for (int f = 0; f < modelo.getRowCount(); f++) {
                for (int c = 0; c < columnas; c++) {
                    Object valor = modelo.getValueAt(f, c);
                    fw.write(escaparCsv(valor == null ? "" : valor.toString()));
                    if (c < columnas - 1) {
                        fw.write(",");
                    }
                }
                fw.write("\n");
            }
            JOptionPane.showMessageDialog(this, "Reporte exportado a:\n" + archivo.getAbsolutePath());
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al exportar CSV: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

